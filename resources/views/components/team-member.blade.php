<script>
let teamIndex = 0;
let isAnimating = false;

function getTeamCardWidth() {
    const card = document.querySelector('.team-card');
    return card ? card.offsetWidth : 0;
}

function getTeamGap() {
    if (window.innerWidth >= 768) return 40; // md
    if (window.innerWidth >= 640) return 32; // sm
    return 24; // default
}

function updateTeamCarousel(animate = true) {
    if (isAnimating && animate) return;
    
    const track = document.querySelector('.team-track');
    const cardWidth = getTeamCardWidth();
    const gap = getTeamGap();
    
    if (animate) {
        isAnimating = true;
        track.style.transition = 'transform 0.7s cubic-bezier(0.4, 0, 0.2, 1)';
        setTimeout(() => {
            isAnimating = false;
        }, 700);
    } else {
        track.style.transition = 'none';
    }
    
    track.style.transform = `translateX(-${teamIndex * (cardWidth + gap)}px)`;
}

function nextTeamMember() {
    if (isAnimating) return;
    
    const totalCards = document.querySelectorAll('.team-card').length;
    teamIndex++;
    
    if (teamIndex >= totalCards) {
        teamIndex = 0;
    }
    
    updateTeamCarousel(true);
}

function previousTeamMember() {
    if (isAnimating) return;
    
    const totalCards = document.querySelectorAll('.team-card').length;
    teamIndex--;
    
    if (teamIndex < 0) {
        teamIndex = totalCards - 1;
    }
    
    updateTeamCarousel(true);
}

// Keyboard navigation
document.addEventListener('keydown', (e) => {
    if (e.key === 'ArrowLeft') {
        previousTeamMember();
    } else if (e.key === 'ArrowRight') {
        nextTeamMember();
    }
});

// Adjust on window resize
let teamResizeTimer;
window.addEventListener('resize', () => {
    clearTimeout(teamResizeTimer);
    teamResizeTimer = setTimeout(() => {
        updateTeamCarousel(false);
    }, 150);
});

// Initialize
updateTeamCarousel(false);
</script>